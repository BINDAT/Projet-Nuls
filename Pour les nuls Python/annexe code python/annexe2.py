# Définit une fonction pour encapsuler le code d'une tâche qui
# accepte deux informations dans des contenants appelés
# variables dont le nom est valeur1 et valeur2.
def ajouteQuelquechose(valeur1,valeur2):

    # Additionne les deux valeurs et retourne le résultat
    # au programme appelant.
    return valeur1 + valeur2

# Appelle la fonction ajouteQuelquechose() avec deux nombres
# pour les additionner puis utiliser print() pour afficher
# le résultat.
print(ajouteQuelquechose(1,2))